function buscarHoteles() {
    const checkIn = document.getElementById('check-in').value;
    const checkOut = document.getElementById('check-out').value;
    const guests = document.getElementById('guests').value;
    const tipoAlojamiento = document.getElementById('tipo-alojamiento').value;

    if (!checkIn || !checkOut) {
        alert('Por favor, selecciona las fechas de llegada y salida.');
        return;
    }
    if (new Date(checkIn) >= new Date(checkOut)) {
        alert('La fecha de salida debe ser posterior a la fecha de llegada.');
        return;
    }

    const resultadosHoteles = document.getElementById('resultados-hoteles');
    resultadosHoteles.innerHTML = ''; // Limpiar resultados previos

    const alojamientosFiltrados = alojamientos.filter(
        (alojamiento) => alojamiento.tipo === tipoAlojamiento && alojamiento.disponible
    );

    if (alojamientosFiltrados.length === 0) {
        resultadosHoteles.innerHTML = '<p>No se encontraron alojamientos disponibles.</p>';
        return;
    }

    alojamientosFiltrados.forEach((alojamiento) => {
        resultadosHoteles.innerHTML += `
            <div class="hotel-card">
                <img src="${alojamiento.imagen}" alt="${alojamiento.nombre}">
                <h2>${alojamiento.nombre}</h2>
                <p>Calificación: ${alojamiento.calificacion}</p>
                <p>${alojamiento.descripcion}</p>
                <p>Cuartos: ${alojamiento.cuartos} • Capacidad: ${alojamiento.capacidad} personas • Baños: ${alojamiento.baños}</p>
                <p>Fechas: ${checkIn} - ${checkOut}</p>
                <p>Huéspedes: ${guests}</p>
                <p>Precio por día: $${alojamiento.precio} USD</p>
                <button onclick="seleccionarAlojamiento('${alojamiento.id}')">Seleccionar</button>
            </div>
        `;
    });
}

function seleccionarAlojamiento(alojamientoId) {
    window.location.href = `detalles.html?alojamientoId=${alojamientoId}`;
}

